    private void PatientSignupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PatientSignupButtonActionPerformed
        // TODO add your handling code here:
        String inputName = PatientNameField.getText();
        String inputIC = PatientICField.getText();
        String inputPhone = PatientPhoneField.getText();
        if (inputName.equals("") || inputIC.equals("")){
            return;
        }
        Patient P = new Patient(inputName,inputIC,inputPhone);
        boolean duplicate = P.CheckPatientDuplicate(inputName, inputIC);
        if (duplicate){
            JOptionPane.showMessageDialog(this, "Name and IC number have been registered");
            PatientNameField.setText("");
            PatientICField.setText("");
            PatientPhoneField.setText("");
        }
        else{
            P.SavePatient();
            JOptionPane.showMessageDialog(this, "Register Success");
            SignUpPanel.setVisible(false);
            LoginPanel.setVisible(true);
            Adminlogin.setVisible(false);
            ChangePassword.setVisible(false);
        }
    }//GEN-LAST:event_PatientSignupButtonActionPerformed


    private void AdminLoginButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdminLoginButton1ActionPerformed
        // TODO add your handling code here:
        String OldPassword = UsernameTextField2.getText();
        char [] NewPasswordArray = AdminPasswordfield1.getPassword();
        String UserName= UsernameTextField3.getText();
        String NewPassword ="";
        
        for (int i = 0; i < NewPasswordArray.length; i++) {
            NewPassword += NewPasswordArray[i];
        }
            Admin A = new Admin();
            if (OldPassword.equals(A.getAdminPassword()) && UserName.equals(A.getAdminUsername())){
                A.SaveAdmin(UserName, NewPassword);
                JOptionPane.showMessageDialog(this, "Password Changed");
                ChangePassword.setVisible(false);
                Adminlogin.setVisible(true);
                LoginPanel.setVisible(false);
                SignUpPanel.setVisible(false);
                
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Invalid Username or Password");
                UsernameTextField2.setText("");
                AdminPasswordfield1.setText("");
                UsernameTextField3.setText("");
            }
            
            
        
        
    }//GEN-LAST:event_AdminLoginButton1ActionPerformed

